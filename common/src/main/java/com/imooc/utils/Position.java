package com.imooc.utils;

public class Position {

    private Float lon, lat; // 经、纬度

    public Float getLon(){
        return lon;
    }

    public Float getLat(){
        return lat;
    }

    public void setLon(Float lon){
        this.lon=lon;
    }

    public void setLat(Float lat){
        this.lat=lat;
    }
}
